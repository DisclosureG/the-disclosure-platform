const hre = require("hardhat");
const fs = require("fs");
const path = require("path");
const { preflight, MAINNET_CHAIN_ID } = require("./_preflight");

// Generated-file paths: the deploy WRITES the exact constructor args it used so
// `hardhat verify --constructor-args <file>` always matches the deployed
// bytecode (ABI-encoded ctor args are part of BscScan's verification hash).
const VERIFY_ARGS_CORE    = path.join(__dirname, "verify-args-core.js");
const VERIFY_ARGS_ARCHIVE = path.join(__dirname, "verify-args-archive.js");

// Write a `module.exports = <value>` generated args file with a clear header.
function writeArgsFile(filePath, header, value) {
  const body =
    "// GENERATED by deploy-consensus.js — do not edit by hand.\n" +
    `// ${header}\n` +
    `module.exports = ${JSON.stringify(value, null, 2)};\n`;
  fs.writeFileSync(filePath, body);
  console.log("Wrote", path.basename(filePath));
}

async function main() {
  // Guards: chainId confirmation (mainnet) + STRIP_REVERTS on real networks.
  const chainId = await preflight();

  const [deployer] = await hre.ethers.getSigners();
  console.log("Deploying EvidenceConsensus with:", deployer.address);

  // Genesis peer set — by default the deployer is the single peer 0 (the
  // documented seed design). Override via env for a multi-peer genesis:
  //   GENESIS_PEERS=0xabc...,0xdef...  GENESIS_HANDLES=Alice,Bob
  const genesisPeers = process.env.GENESIS_PEERS
    ? process.env.GENESIS_PEERS.split(",").map(s => s.trim()).filter(Boolean)
    : [deployer.address];
  const genesisHandles = process.env.GENESIS_HANDLES
    ? process.env.GENESIS_HANDLES.split(",").map(s => s.trim()).filter(Boolean)
    : ["Genesis"];
  if (genesisPeers.length !== genesisHandles.length) {
    throw new Error(
      `genesisPeers (${genesisPeers.length}) and genesisHandles ` +
      `(${genesisHandles.length}) length mismatch — they must be paired 1:1.`,
    );
  }
  console.log("Genesis peers:", genesisPeers);
  console.log("Genesis handles:", genesisHandles);

  // Seed-phase K — minimum activePeerCount before public nominatePeer unlocks.
  // While count < K, only `addPeer` (owner) can add peers.  Closes the
  // Genesis-1 Sybil window.  Override with SEED_PHASE_K env var.
  const seedPhaseK = Number(process.env.SEED_PHASE_K ?? 5);
  console.log("Seed-phase K:", seedPhaseK);

  // Clean + recompile before reading the artifact so the size check (and the
  // bytecode we actually deploy) reflects current source AND the STRIP_REVERTS
  // flag — never a stale artifact left from a full-strings build.
  await hre.run("clean");
  await hre.run("compile");

  const Factory = await hre.ethers.getContractFactory("EvidenceConsensus");

  // Guard against shipping an oversized contract: EIP-170 caps deployed bytecode
  // at 24576 bytes. The full-strings build is ~27.5 KB; STRIP_REVERTS=1 brings it
  // under. Fail fast with a clear instruction rather than a cryptic on-chain revert.
  const runtimeBytes = (hre.artifacts.readArtifactSync("EvidenceConsensus").deployedBytecode.length - 2) / 2;
  console.log("Runtime bytecode size:", runtimeBytes, "bytes (limit 24576)");
  if (runtimeBytes > 24576) {
    throw new Error(
      `Deployed bytecode is ${runtimeBytes} bytes (> 24576). Re-run with STRIP_REVERTS=1, ` +
      `e.g. STRIP_REVERTS=1 npx hardhat run scripts/deploy-consensus.js --network ${hre.network.name}`,
    );
  }

  const contract = await Factory.deploy(genesisPeers, genesisHandles, seedPhaseK);
  await contract.waitForDeployment();

  const addr = await contract.getAddress();
  console.log("EvidenceConsensus deployed to:", addr);

  // Single source of truth for verify: persist the EXACT ctor args used here.
  writeArgsFile(
    VERIFY_ARGS_CORE,
    "EvidenceConsensus core ctor args: (genesisPeers, genesisHandles, seedPhaseK).",
    [genesisPeers, genesisHandles, seedPhaseK],
  );

  // Peer-governance sidecar: holds the nominee + revocation flows moved off the
  // core to stay under EIP-170. It mutates the core's peer set through the
  // governance-gated gAddPeer/gRemovePeer, wired once via setGovernance below.
  const GovFactory = await hre.ethers.getContractFactory("PeerGovernance");
  const gov = await GovFactory.deploy(addr);
  await gov.waitForDeployment();
  const govAddr = await gov.getAddress();
  console.log("PeerGovernance deployed to:", govAddr);

  // One-shot wiring: link the governance contract so it can admit/revoke peers.
  // setGovernance is irreversible (core require(governance==address(0))).
  const wireTx = await contract.setGovernance(govAddr);
  // Wait for extra confirmations on mainnet so the one-shot write is final.
  await (chainId === MAINNET_CHAIN_ID ? wireTx.wait(3) : wireTx.wait());
  // Verify the wiring actually landed before continuing.
  const wired = await contract.governance();
  if (wired.toLowerCase() !== govAddr.toLowerCase()) {
    throw new Error(`setGovernance failed: core.governance()=${wired}, expected ${govAddr}`);
  }
  console.log("Wired core.governance ->", govAddr);

  // Read-only Lens sidecar: holds the peer/nominee/proposal aggregation views
  // moved off the core to stay under EIP-170. No storage, no privileges — it
  // only reads the core's and governance's public state. Deployed after both.
  const LensFactory = await hre.ethers.getContractFactory("EvidenceConsensusLens");
  const lens = await LensFactory.deploy(addr, govAddr);
  await lens.waitForDeployment();
  const lensAddr = await lens.getAddress();
  console.log("EvidenceConsensusLens deployed to:", lensAddr);

  // On-chain archive sidecar: stores the human-readable strings the core only
  // commits as hashes (evidence content, node meta, note text), each verified
  // against the core's hash. Makes the chain a complete backup; no privileges.
  const ArchiveFactory = await hre.ethers.getContractFactory("EvidenceArchive");
  const archive = await ArchiveFactory.deploy(addr);
  await archive.waitForDeployment();
  const archiveAddr = await archive.getAddress();
  console.log("EvidenceArchive deployed to:", archiveAddr);

  // Persist the archive's single ctor arg (the core address) for verify.
  writeArgsFile(
    VERIFY_ARGS_ARCHIVE,
    "EvidenceArchive ctor arg: the live EvidenceConsensus core address.",
    [addr],
  );

  const deployBlock = await hre.ethers.provider.getBlockNumber();

  console.log("\n=== Frontend (.env / Netlify) ===");
  console.log("VITE_CONSENSUS_ADDR=" + addr);
  console.log("VITE_CONSENSUS_GOVERNANCE_ADDR=" + govAddr);
  console.log("VITE_CONSENSUS_LENS_ADDR=" + lensAddr);
  console.log("VITE_CONTENT_ARCHIVE_ADDR=" + archiveAddr);
  console.log("VITE_DEPLOY_BLOCK=" + deployBlock);

  console.log("\n=== Supabase edge secrets ===");
  console.log("CONSENSUS_ADDR=" + addr);
  console.log("GOVERNANCE_ADDR=" + govAddr);
  console.log("CONTENT_ARCHIVE_ADDR=" + archiveAddr);
  console.log("CONSENSUS_RPC_URL=<your BSC RPC URL>          (set/confirm)");
  console.log("CONSENSUS_CHAIN_ID=" + chainId.toString() + "                          (set/confirm)");

  console.log("\n=== Verify (must use STRIP_REVERTS=1) ===");
  console.log(`STRIP_REVERTS=1 npx hardhat verify --network ${hre.network.name} --constructor-args scripts/verify-args-core.js ${addr}`);
  console.log(`STRIP_REVERTS=1 npx hardhat verify --network ${hre.network.name} ${govAddr} ${addr}`);
  console.log(`STRIP_REVERTS=1 npx hardhat verify --network ${hre.network.name} ${lensAddr} ${addr} ${govAddr}`);
  console.log(`STRIP_REVERTS=1 npx hardhat verify --network ${hre.network.name} --constructor-args scripts/verify-args-archive.js ${archiveAddr}`);
}

main().catch(err => { console.error(err); process.exit(1); });
